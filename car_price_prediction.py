df['Fuel_Type'] = df['Fuel_Type'].replace({
    'Petrol': 0,
    'Diesel': 1,
    'CNG': 2
})

df['Selling_type'] = df['Selling_type'].map({
    'Dealer': 0,
    'Individual': 1
})

df['Transmission'] = df['Transmission'].map({
    'Manual': 0,
    'Automatic': 1
})

from sklearn.preprocessing import LabelEncoder
from sklearn.metrics import mean_squared_error, r2_score
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LinearRegression

x = df[['Year','Present_Price', 'Driven_kms',
       'Fuel_Type', 'Selling_type', 'Transmission']]
y = df['Selling_Price']

x_train,x_test,y_train,y_test = train_test_split(x,y, test_size=0.2, random_state= 42)

lr = LinearRegression()
lr.fit(x_train,y_train)

lr_model = lr.predict(x_test)

print("R2 Score:", r2_score(y_test, lr_model))

print("MSE:", mean_squared_error(y_test, lr_model))

plt.figure(figsize=(10,5))
plt.plot(y_test.values, label='Actual Prices')
plt.plot(lr_model, label='Predicted Prices')
plt.legend()
plt.title("Actual vs Predicted Prices")
plt.show()